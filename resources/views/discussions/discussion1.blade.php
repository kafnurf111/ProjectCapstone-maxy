    <script>
        document.querySelector('.formUploadFile').addEventListener('submit', async function(event) {
            event.preventDefault();

            const formData = new FormData(this);

            try {
                const response = await fetch('/upload', {
                    method: 'POST',
                    body: formData
                });

                const data = await response.json();
                const uploadStatus = document.getElementById('upload-status');
                uploadStatus.textContent = data.message; // Menampilkan pesan respons dari server
            } catch (error) {
                console.error('Error:', error);
            }
        });

        document.getElementById('file-label').addEventListener('click', function() {
            document.getElementById('div-file').style.display = 'block';
            document.getElementById('button-file').style.display = 'none';
            document.getElementById('file').focus(); // Fokus pada input file setelah ditampilkan
        });

        // Fungsi untuk menampilkan nama file yang dipilih
        function displayFileNames(input) {
            const fileCountContainer = document.getElementById('file-count');
            const fileNamesContainer = document.getElementById('file-names');
            fileNamesContainer.innerHTML = ''; // Kosongkan kontainer sebelum menambahkan file baru

            if (input.files.length > 0) {
                // Menampilkan jumlah file yang dipilih
                fileCountContainer.textContent = `${input.files.length} file(s) selected`;

                // Menampilkan daftar nama file
                const fileList = document.createElement('ul');
                for (let i = 0; i < input.files.length; i++) {
                    const listItem = document.createElement('li');
                    listItem.textContent = input.files[i].name;
                    fileList.appendChild(listItem);
                }
                fileNamesContainer.appendChild(fileList);
            } else {
                fileCountContainer.textContent = '';
                fileNamesContainer.textContent = '';
            }
        }
    </script>
